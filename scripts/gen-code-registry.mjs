#!/usr/bin/env node
// gen-code-registry.mjs — the DERIVED code registry (standard #10 "derived catalog", #219; closes cluster ①).
//
// The registry is GENERATED from the code-index (the single source of truth), NOT hand-curated — so every
// emittable code is registered BY CONSTRUCTION. This is the universal-coverage anchor for the codes dimension:
// "every governable construct indexed by >=1 audit" becomes true-by-build, the way rustc derives its error
// index from `error_codes!` rather than maintaining a parallel hand list.
//
// Each code is tagged: live (emitted w/ exported const) · inline (emitted, no const — R4) · dead (defined,
// never emitted — RESERVED, standard #1 wire-or-retire) · phantom (doc-only — drift, DOC-004) · ref (mention
// only). Output: build/code-registry/REGISTRY.md (browsable catalog) + registry.json (machine, for audit-coverage).
// Run after code-index.mjs:  node scripts/code-index.mjs && node scripts/gen-code-registry.mjs
import { readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
let arr;
try {
  arr = JSON.parse(readFileSync(join(ROOT, "build/code-index/code-index.json"), "utf8"));
} catch {
  console.error("gen-code-registry: build/code-index/code-index.json missing — run `node scripts/code-index.mjs` first.");
  process.exit(2);
}

const statusOf = (c) => {
  const defs = (c.defs || []).length, emits = (c.emits || []).length;
  if (c.docOnly) return "phantom";        // doc-only mention, never in source
  if (defs > 0 && emits === 0) return "dead";   // defined, never emitted (RESERVED)
  if (emits > 0 && defs === 0) return "inline"; // emitted, no exported constant (R4)
  if (emits > 0) return "live";
  return "ref";                           // referenced only
};

const entries = arr.map((c) => ({
  code: c.code, family: c.family, namespace: c.namespace, status: statusOf(c),
  names: c.names || [], severities: c.severities || [],
  defs: (c.defs || []).length, emits: (c.emits || []).length, tests: c.tests || 0, docs: c.docs || 0,
})).sort((a, b) => a.code.localeCompare(b.code));

const counts = {};
for (const e of entries) counts[e.status] = (counts[e.status] || 0) + 1;
const byFamily = {};
for (const e of entries) (byFamily[e.family] ??= []).push(e);

mkdirSync(join(ROOT, "build/code-registry"), { recursive: true });
writeFileSync(join(ROOT, "build/code-registry/registry.json"),
  JSON.stringify({ generatedFrom: "build/code-index/code-index.json", total: entries.length, counts, entries }, null, 2));

const md = [
  "# LogicN — Derived Code Registry (generated by scripts/gen-code-registry.mjs)", "",
  "The authoritative catalog of EVERY diagnostic/error code, GENERATED from the code-index (standard #10).",
  "Do NOT hand-edit. Regenerate: `node scripts/code-index.mjs && node scripts/gen-code-registry.mjs`.",
  "Universal-coverage anchor (#219 std 1): every code below is registered by construction — no orphans.", "",
  "## Status counts", "",
  "| status | count | meaning |", "|---|---|---|",
  `| live | ${counts.live || 0} | emitted with an exported constant |`,
  `| inline | ${counts.inline || 0} | emitted, NO exported constant (R4 — Stage F) |`,
  `| dead | ${counts.dead || 0} | defined, never emitted — RESERVED (wire or retire, std #1) |`,
  `| phantom | ${counts.phantom || 0} | doc-only mention, not in source (drift — DOC-004) |`,
  `| ref | ${counts.ref || 0} | referenced only (no def/emit) |`, "",
  "## RESERVED — defined but not emitted (std #1: tag wire-or-retire)", "",
  entries.filter((e) => e.status === "dead").map((e) => `- \`${e.code}\``).join("\n") || "(none)", "",
  "## Catalog (by family)", "",
];
for (const fam of Object.keys(byFamily).sort()) {
  md.push(`### ${fam} (${byFamily[fam].length})`, "", "| code | status | name(s) | severity |", "|---|---|---|---|");
  for (const e of byFamily[fam]) md.push(`| ${e.code} | ${e.status} | ${e.names.join(" / ") || "—"} | ${e.severities.join("/") || "—"} |`);
  md.push("");
}
writeFileSync(join(ROOT, "build/code-registry/REGISTRY.md"), md.join("\n"));

console.log(`gen-code-registry: ${entries.length} codes — ${Object.entries(counts).map(([k, v]) => `${k}:${v}`).join(" · ")}`);
console.log("  -> build/code-registry/REGISTRY.md + registry.json");
